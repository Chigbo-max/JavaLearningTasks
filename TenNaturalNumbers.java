public class SumOfTenNaturalNumbers{

	public static void main(String... args){

	int totalNaturalNumbers = 0;
	int naturalNumberCounter = 1;

	while(naturalNumberCounter!=11){
	totalNaturalNumbers = totalNaturalNumbers + naturalNumberCounter;
	naturalNumberCounter++;
	}
	System.out.println("The sum of the first 10 natural numbers is" + " " + 	totalNaturalNumbers);














}









}