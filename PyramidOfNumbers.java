public class PyramidOfNumbers{
	public static void main(String... args){


			for(int k = 7; k>0; k--){
			System.out.print(k);

			for(int i = 6; i < 1; i--){
			System.out.print(i);

			for(int j = 1; j<=k; j++){
			
			System.out.print(j);

				}
				
			
			System.out.println();
			
		}
	}
	


	}




}