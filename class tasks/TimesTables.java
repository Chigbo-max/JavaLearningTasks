import java.util.Scanner;

public class TimesTables{
	public static void main(String... args){
	
	for(int count = 1; count < 10; count++){
		for(int counter = 1; counter <10; counter++){

		System.out.print(count + " * " + counter + " " + "= " + (counter*count) + "\t");

	}
	System.out.println(" ");

	}
	

}

}