public class NaturalNumbers{

	public static void main(String... args){
	
	int sumOfFirstTen = 0;
	int naturalNumberCounter=1;

	while(naturalNumberCounter<=10){
		sumOfFirstTen = sumOfFirstTen + naturalNumberCounter;
		naturalNumberCounter++;
	}
	System.out.println("The sum of the first ten natural numbers is:"+" "+ sumOfFirstTen);


}





}